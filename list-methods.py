boys = ['John', 'David', 'Nick', 'Martin', 'Tom']
print(boys[0]) # John
print(boys[0:2]) # ['John', 'David']

# inverse list
print(boys[::-1]) # ['Tom', 'Martin', 'Nick', 'David', 'John']

# copy list
s1 = boys[:]

# join lists
boys = ['John', 'David', 'Nick', 'Martin', 'Tom']
girls = ['Alice', 'Eva', 'Linda', 'Mary', 'Sara']
print(boys + girls) # ['John', 'David', 'Nick', 'Martin', 'Tom', 'Alice', 'Eva', 'Linda', 'Mary', 'Sara']
